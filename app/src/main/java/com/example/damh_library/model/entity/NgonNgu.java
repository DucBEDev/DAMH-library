package com.example.damh_library.model.entity;

public class NgonNgu {
    private String maNgonNgu;
    private String ngonNgu;

    public NgonNgu() {
    }

    public NgonNgu(String maNgonNgu, String ngonNgu) {
        this.maNgonNgu = maNgonNgu;
        this.ngonNgu = ngonNgu;
    }

    public String getMaNgonNgu() {
        return maNgonNgu;
    }

    public void setMaNgonNgu(String maNgonNgu) {
        this.maNgonNgu = maNgonNgu;
    }

    public String getngonNgu() {
        return ngonNgu;
    }

    public void setngonNgu(String ngonNgu) {
        this.ngonNgu = ngonNgu;
    }
}
